	MinecraftMaterial(Material material, String id) {
		this.material = material;
		this.UUID = id;
	}
	
	public static MinecraftMaterial getMinecraftMaterialFromUUID(String id) {
		for (MinecraftMaterial m : values())
			if (m.getUUID().equalsIgnoreCase(id))
				return m;
		return MinecraftMaterial.AIR;
	}
	
	public static String getMinecraftMaterialFromItemStack(ItemStack i) {
		for (MinecraftMaterial m : values())
			if (m.getMaterial().equals(i.getType()))
				return m.getUUID();
		return MinecraftMaterial.AIR.getUUID();
	}
